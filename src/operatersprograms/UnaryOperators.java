package operatersprograms;

public class UnaryOperators {

	public static void main(String[] args) {
		
		int a= 10,b=20,c=30,d=40;
		
		System.out.println(a++);
		System.out.println(++b);
		System.out.println(c--);
		System.out.println(--d);
		System.out.println("------------------------");
		
		a++;
		++b;
		c--;
		--d;
		
		System.out.println(a);
		System.out.println(b);
		System.out.println(c);
		System.out.println(d); 
		
		

	}

}
