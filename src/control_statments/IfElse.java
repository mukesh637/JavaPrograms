package control_statments;

public class IfElse {

	public static void main(String[] args) {
		int i=200, j=300;
		
		System.out.println("Entry point");
		
		if(i<500)
		{
			System.out.println("i value is less than j");
		}
		else
		{
			System.out.println("i value is not equals to j");
		}
		
		System.out.println("End point");

	}

}
